package mysumproject;

/**
 *
 * @author Melaku
 */
public class Vehicle {
    private double gallonOfGas;
    private int mpg;

    /**
     * @return the gallonOfGas
     */
    public double getGallonOfGas() {
        return gallonOfGas;
    }

    /**
     * @param gallonOfGas the gallonOfGas to set
     */
    public void setGallonOfGas(double gallonOfGas) {
        this.gallonOfGas = gallonOfGas;
    }

    /**
     * @return the mpg
     */
    public int getMpg() {
        return mpg;
    }

    /**
     * @param mpg the mpg to set
     */
    public void setMpg(int mpg) {
        this.mpg = mpg;
    }
}
